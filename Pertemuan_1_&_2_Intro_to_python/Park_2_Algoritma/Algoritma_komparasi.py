x = 2
y = 2

# besar
hasil = (x > y)
print(f"Apakah nilai dari {x} besar dari {y} adalah {hasil} ")
# besar sama dengan
hasil = (x >= y)
print(f"Apakah nilai dari {x} besar sama dengan dari {y} adalah {hasil} ")

# kecil
hasil = (x < y)
print(f"Apakah nilai dari {x} kecil dari {y} adalah {hasil} ")
# kecil sama dengan
hasil = (x <= y)
print(f"Apakah nilai dari {x} kecil sama dengan dari {y} adalah {hasil} ")

